// Détection langue navigateur
export function detectUserLanguage() {
  if (typeof window === 'undefined') return 'fr'
  
  const browserLang = navigator.language || navigator.userLanguage
  const langCode = browserLang.toLowerCase().split('-')[0]
  const countryCode = browserLang.toLowerCase().split('-')[1]
  
  const countryMap = {
    'us': 'us', 'gb': 'en', 'ca': 'ca', 'au': 'au',
    'in': 'in', 'sg': 'sg', 'br': 'br', 'se': 'se',
    'nl': 'nl', 'de': 'de', 'es': 'es', 'it': 'it',
    'pt': 'pt', 'fr': 'fr'
  }
  
  if (countryCode && countryMap[countryCode]) return countryMap[countryCode]
  if (langCode && translations[langCode]) return langCode
  return 'fr'
}

// Hook traduction
export function useTranslation(lang = 'fr') {
  return (key) => translations[lang]?.[key] || translations.fr[key] || key
}
