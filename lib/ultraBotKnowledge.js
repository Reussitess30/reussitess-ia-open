// Fonction de recherche ultra intelligente
export function searchUltraKnowledge(query, context = {}) {
  const q = query.toLowerCase();
  const results = {
    type: "general",
    data: null,
    emotion: detectEmotion(query),
    easterEgg: checkEasterEgg(query),
  };

  // Détecter Easter Eggs
  if (results.easterEgg) {
    results.type = "easterEgg";
    return results;
  }

  // Détecter émotion
  if (results.emotion) {
    results.type = "emotion";
    return results;
  }

  // Recherche bibliothèque
  if (q.includes("guadeloupe") || q.includes("971")) {
    results.type = "bibliotheque";
    results.data = ULTRA_KNOWLEDGE.bibliotheque.domTom.guadeloupe;
  }

  if (q.includes("martinique") || q.includes("972")) {
    results.type = "bibliotheque";
    results.data = ULTRA_KNOWLEDGE.bibliotheque.domTom.martinique;
  }

  // Blagues
  if (q.includes("blague") || q.includes("rigole") || q.includes("mdr")) {
    results.type = "blague";
    results.data =
      ULTRA_KNOWLEDGE.personnalite.blagues[
        Math.floor(Math.random() * ULTRA_KNOWLEDGE.personnalite.blagues.length)
      ];
  }

  // Citations
  if (
    q.includes("citation") ||
    q.includes("inspiration") ||
    q.includes("motiv")
  ) {
    results.type = "citation";
    results.data =
      ULTRA_KNOWLEDGE.citations[
        Math.floor(Math.random() * ULTRA_KNOWLEDGE.citations.length)
      ];
  }

  return results;
}

function detectEmotion(query) {
  const q = query.toLowerCase();

  if (
    q.includes("triste") ||
    q.includes("😢") ||
    q.includes("mal") ||
    q.includes("déprim")
  ) {
    return "tristesse";
  }
  if (
    q.includes("content") ||
    q.includes("😊") ||
    q.includes("heureux") ||
    q.includes("🎉")
  ) {
    return "joie";
  }
  if (q.includes("doute") || q.includes("peur") || q.includes("inquiet")) {
    return "doute";
  }
  if (q.includes("fatigué") || q.includes("épuisé") || q.includes("😴")) {
    return "fatigue";
  }
  if (q.includes("fier") || q.includes("réussi") || q.includes("gagné")) {
    return "fierte";
  }

  return null;
}

function checkEasterEgg(query) {
  const q = query.toLowerCase();

  for (const [key, value] of Object.entries(ULTRA_KNOWLEDGE.easterEggs)) {
    if (q.includes(key)) {
      return value;
    }
  }

  return null;
}

