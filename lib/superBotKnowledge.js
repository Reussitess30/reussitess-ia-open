// Fonction de recherche intelligente
export function searchSuperKnowledge(query) {
  const q = query.toLowerCase();
  const results = [];

  // Quiz
  if (q.includes("quiz") || q.includes("question") || q.includes("test")) {
    results.push({
      type: "quiz",
      data: SUPER_KNOWLEDGE.quiz,
    });
  }

  // Astuces Amazon
  if (q.includes("astuce") || q.includes("amazon") || q.includes("conseil")) {
    results.push({
      type: "astuces",
      data: SUPER_KNOWLEDGE.astucesAmazon,
    });
  }

  // Proverbes
  if (q.includes("proverbe") || q.includes("sagesse") || q.includes("créole")) {
    results.push({
      type: "proverbes",
      data: SUPER_KNOWLEDGE.proverbesAntillais,
    });
  }

  // Politiques
  if (
    q.includes("politique") ||
    q.includes("césaire") ||
    q.includes("histoire")
  ) {
    results.push({
      type: "politiques",
      data: SUPER_KNOWLEDGE.politiquesAntillais,
    });
  }

  // Sécurité
  if (
    q.includes("sécurité") ||
    q.includes("securite") ||
    q.includes("hack") ||
    q.includes("protéger")
  ) {
    results.push({
      type: "securite",
      data: SUPER_KNOWLEDGE.securite,
    });
  }

  // Actualités
  if (q.includes("actu") || q.includes("news") || q.includes("info")) {
    results.push({
      type: "actualites",
      data: SUPER_KNOWLEDGE.actualites,
    });
  }

  return results;
}

